// ─── Utility: hex-encode a byte buffer ────────────────────────────────────────
static std::string hex_encode(const uint8_t* buf, size_t len) {
    std::ostringstream oss;
    oss << std::hex << std::setfill('0');
    for (size_t i = 0; i < len; ++i)
        oss << std::setw(2) << static_cast<unsigned>(buf[i]);
    return oss.str();
}

