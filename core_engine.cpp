static caelus::causal::EngineSnapshot run_repl_tick(
    caelus::causal::CausalEngine& engine) {
    const uint64_t tick_before = engine.current_tick();
    g_tick_ctr.store(tick_before, std::memory_order_relaxed);
    const size_t delivered = g_registry.dispatch_connectors();
    if (delivered > 0) {
        std::cout << "[CONNECTOR] Tick " << tick_before << ": "
                  << delivered << " intel olayi enjekte edildi.\n";
    }
    audit_intel_rejections();

    caelus::causal::EngineSnapshot snap = engine.run_ticks(1);
    g_emitter.emit(caelus::ws_json::friction(
        snap.clamped_friction_d(),
        snap.outage_active ? 3 : (snap.regime_exceeded ? 2 : 0),
        engine.current_tick()));

    if (g_audit.is_open()) {
        std::ostringstream ss;
        ss << std::fixed << std::setprecision(6);
        ss << "{\"type\":\"REPL_TICK\","
           << "\"tick_before\":" << tick_before
           << ",\"tick_after\":" << engine.current_tick()
           << ",\"raw_friction\":" << snap.raw_friction_d()
           << ",\"clamped_friction\":" << snap.clamped_friction_d()
           << ",\"regime_exceeded\":" << (snap.regime_exceeded ? "true" : "false")
           << ",\"outage\":" << (snap.outage_active ? "true" : "false")
           << ",\"deadline_missed\":" << (snap.any_deadline_missed ? "true" : "false")
           << ",\"hysteresis_flip\":" << (snap.any_hysteresis_flip ? "true" : "false")
           << "}";
        g_audit.append(ss.str());
    }

    // War Room canlı yayını: throughput göstergesi + gerçek graf snapshot'ı.
    // Yalnız bir tarayıcı bağlıyken serileştirme maliyetine gireriz.
    if (g_emitter.connected()) {
        g_emitter.emit(caelus::ws_json::engine_state(
            g_scenario_id.empty() ? "LIVE" : g_scenario_id, g_scenario_id,
            engine.current_tick(), snap.clamped_friction_d(),
            snap.throughput_ratio, snap.outage_active, snap.any_hysteresis_flip));
        emit_ws_snapshot(engine, g_scenario_id);
    }

    return snap;
}

static caelus::causal::EngineSnapshot RunScenarioRepl(
    caelus::causal::CausalEngine& engine,
    const caelus::ScenarioPack& pack,
    bool pack_loaded,
    const std::string& scenario_id,
    caelus::causal::EngineSnapshot last_snap) {
    std::cout << "\n[REPL] Senaryo REPL basladi. Yardim icin 'help', cikis icin 'quit'.\n";
    print_repl_snapshot(engine, last_snap);

    // War Room tarayıcısından gelen komutları (WS text frame) da bu REPL'e
    // yönlendir: gerçek motor sürülür, snapshot geri yayınlanır. Handler
    // emitter thread'inde koşar, sadece kuyruğa iter (motora dokunmaz).
    g_emitter.on_command([](std::string c){ g_repl_cmds.push(std::move(c)); });

    // stdin ayrı bir thread'de okunup aynı kuyruğa beslenir; ana döngü tek
    // tüketici olarak terminal + tarayıcı komutlarını sırayla işler.
    std::thread stdin_thread([]{
        std::string in;
        while (std::getline(std::cin, in)) g_repl_cmds.push(in);
        g_repl_cmds.close();
    });
    stdin_thread.detach();

    std::string line;
    while (true) {
        std::cout << "\ncaelus:" << scenario_id << " t=" << engine.current_tick() << "> ";
        if (!g_repl_cmds.pop(line)) {
            std::cout << "\n[REPL] stdin kapandi, cikiliyor.\n";
            break;
        }

        const std::string command_line = trim_copy(line);
        if (command_line.empty()) continue;

        std::istringstream iss(command_line);
        std::string cmd;
        iss >> cmd;
        std::transform(cmd.begin(), cmd.end(), cmd.begin(),
                       [](unsigned char c){ return static_cast<char>(std::tolower(c)); });

        if (cmd == "quit" || cmd == "exit") {
            audit_repl_event(command_line, engine, "quit");
            std::cout << "[REPL] Cikis.\n";
            break;
        }
        if (cmd == "help" || cmd == "?") {
            print_repl_help();
            audit_repl_event(command_line, engine, "ok");
            continue;
        }
        if (cmd == "status" || cmd == "snapshot") {
            std::string format;
            iss >> format;
            std::transform(format.begin(), format.end(), format.begin(),
                           [](unsigned char c){ return static_cast<char>(std::tolower(c)); });
            if (format == "--json" || format == "json") {
                print_repl_snapshot_json(scenario_id, engine, last_snap);
            } else {
                print_repl_snapshot(engine, last_snap);
            }
            // War Room "ANALİZ ET" bu komutu gönderir: gerçek graf snapshot'ını
            // WS üzerinden geri yayınla (analiz paneli motorun canlı verisiyle dolar).
            if (g_emitter.connected()) emit_ws_snapshot(engine, g_scenario_id);
            audit_repl_event(command_line, engine, "ok");
            continue;
        }
        if (cmd == "list") {
            std::string what;
            iss >> what;
            std::transform(what.begin(), what.end(), what.begin(),
                           [](unsigned char c){ return static_cast<char>(std::tolower(c)); });
            if (what == "levers") {
                print_repl_levers(pack, pack_loaded);
                // War Room lever konsolu: gerçek kaldıraç listesini WS'e yayınla
                // (UI'daki hardcoded ipuçları yerine motorun paketinden gelir).
                if (g_emitter.connected()) {
                    std::ostringstream lo;
                    lo << "{\"type\":\"levers\",\"scenario_id\":\""
                       << json_escape(g_scenario_id) << "\",\"levers\":[";
                    bool lf = true;
                    if (pack_loaded) {
                        for (const auto& lv : pack.levers) {
                            if (!lf) lo << ",";
                            lf = false;
                            lo << "{\"id\":\"" << json_escape(lv.id) << "\","
                               << "\"target\":\"" << json_escape(lv.target) << "\","
                               << "\"success_p\":" << std::fixed << std::setprecision(2)
                               << caelus::causal::fp_to_d(lv.success_p_fp) << ","
                               << "\"cost_ticks\":" << lv.cost_ticks << ","
                               << "\"lockout_ticks\":" << lv.lockout_ticks << "}";
                        }
                    }
                    lo << "]}";
                    g_emitter.emit(lo.str());
                }
                audit_repl_event(command_line, engine, "ok");
            } else {
                std::cout << "[REPL] Bilinmeyen liste. Ornek: list levers\n";
                audit_repl_event(command_line, engine, "error");
            }
            continue;
        }
        if (cmd == "tick") {
            uint64_t n = 1;
            if (iss >> n) {
                if (n == 0 || n > 100000) {
                    std::cout << "[REPL] tick sayisi 1..100000 araliginda olmali.\n";
                    audit_repl_event(command_line, engine, "error");
                    continue;
                }
            }

            for (uint64_t i = 0; i < n; ++i) {
                last_snap = run_repl_tick(engine);
            }
            std::cout << "[REPL] " << n << " tick tamamlandi.\n";
            print_repl_snapshot(engine, last_snap);
            audit_repl_event(command_line, engine, "ok");
            continue;
        }
        if (cmd == "lever") {
            std::string lever_id;
            iss >> lever_id;
            if (lever_id.empty()) {
                std::cout << "[REPL] Kullanim: lever <id>\n";
                audit_repl_event(command_line, engine, "error");
                continue;
            }
            const caelus::causal::Lever* meta =
                pack_loaded ? find_pack_lever(pack, lever_id) : nullptr;
            if (!meta) {
                std::cout << "[REPL] Kaldirac bulunamadi: " << lever_id
                          << " (liste icin: list levers)\n";
                audit_repl_event(command_line, engine, "error");
                continue;
            }

            const bool success = engine.apply_lever(lever_id);
            const int remaining_cost =
                meta->cost_ticks > 1 ? (meta->cost_ticks - 1) : 0;
            std::cout << "[REPL] Lever sonucu: " << (success ? "BASARILI" : "BASARISIZ/KILITLI")
                      << " | cost_ticks=" << meta->cost_ticks
                      << " (tam maliyet icin ek tick: " << remaining_cost << ")\n";
            last_snap = run_repl_tick(engine);
            print_repl_snapshot(engine, last_snap);
            audit_repl_event(command_line, engine, success ? "success" : "failed_or_locked");
            continue;
        }

        std::cout << "[REPL] Bilinmeyen komut: " << cmd << " (yardim: help)\n";
        audit_repl_event(command_line, engine, "error");
    }

    return last_snap;
}

static int PrintScenarioCanonicalPayload(const std::string& path) {
    std::string payload;
    std::string error;
    if (!caelus::ScenarioPack::canonical_signed_payload_from_file(path, payload, &error)) {
        std::cerr << "[SIGNER] Canonical payload üretilemedi: " << error << "\n";
        return 2;
    }
    std::cout << payload;
    return 0;
}

static int ReportScenarioSignerBlocked(const std::string& path,
                                       const std::string& key_path) {
    (void)key_path;
    std::string payload;
    std::string error;
    if (!caelus::ScenarioPack::canonical_signed_payload_from_file(path, payload, &error)) {
        std::cerr << "[SIGNER] Imzalanacak payload üretilemedi: " << error << "\n";
        return 2;
    }
    std::cerr
        << "[SIGNER] BLOCKER: Bu binary içinde export edilmiş ed25519 signing FFI sembolü yok.\n"
        << "         Mevcut Rust FFI yalnız doğrulama sağlıyor "
        << "(caelus_verify_scenario_signature); C++ tarafında seed ile imza atacak\n"
        << "         hazır fonksiyon bulunmadığı için iki dosya sınırında gerçek imza üretilemiyor.\n"
        << "         Payload almak için: --print-scenario-payload <json>\n";
    return 3;
}

