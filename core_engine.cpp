/// Extract embedded UI to a temp path so a local browser can open it.
/// The files are DELETED on process exit (see cleanup in main) so no cleartext
/// UI persists after the session — matching the air-gapped claim.
static bool DumpEmbeddedUI(const std::string& html_path,
                           const std::string& compiler_path,
                           const std::string& js_path) {
    auto write_bytes = [](const std::string& path,
                          const unsigned char* data, std::size_t len) -> bool {
        FILE* f = std::fopen(path.c_str(), "wb");
        if (!f) return false;
        std::fwrite(data, 1, len, f);
        std::fclose(f);
        return true;
    };
    bool ok = true;
    ok &= write_bytes(html_path, CAELUS_UI_HTML, CAELUS_UI_HTML_LEN);
    ok &= write_bytes(compiler_path, CAELUS_SCENARIO_COMPILER_JS,
                      CAELUS_SCENARIO_COMPILER_JS_LEN);
    ok &= write_bytes(js_path,   CAELUS_UI_JS,   CAELUS_UI_JS_LEN);
    return ok;
}
