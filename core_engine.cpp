// ─── Solver Plugin Dispatch  ──────────────────────────────────────────────────
/**
 * RunOptimizationCycle
 *
 * Dispatches to the active solver plugin via g_registry.solve().
 * The solver (DeterministicSolver or ORToolsSolver) is selected at bootstrap
 * and hidden behind the plugin ABI — this function contains no solver logic.
 *
 * Adding a new solver:
 *   1. Implement struct MySolver : SolverBase<MySolver> { ... };
 *   2. Call g_registry.set_solver(MySolver::make_vtable()) in main().
 *   Done — this function needs no changes.
 */
static caelus::SolverResult RunOptimizationCycle(double friction_multiplier) {
    using caelus::intel::FRICTION_MULTIPLIER_MAX;
    using caelus::intel::FRICTION_MULTIPLIER_MIN;

    // Defense in depth: clamp before handing to the plugin.
    friction_multiplier = std::clamp(friction_multiplier,
                                     FRICTION_MULTIPLIER_MIN, FRICTION_MULTIPLIER_MAX);

    // Emit the friction to the War Room before solving.
    g_emitter.emit(caelus::ws_json::friction(friction_multiplier, 0, 0));

    // Build the solver request (domain-agnostic timing constants).
    caelus::SolverRequest req;
    req.friction_multiplier   = friction_multiplier;
    req.task_start_min        = 360;  // 06:00
    req.target_deadline_min   = 480;  // 08:00
    req.commit_overhead_min   = 30;
    req.base_transit_low_min  = 45;
    req.base_transit_high_min = 65;

    std::cout << "\n[CORE] -- Solver Plugin [" << g_registry.solver_name()
              << "] ----------------\n";
    std::cout << "[CORE] Cycle: Generic constrained transfer\n";
    std::cout << "[CORE] Task starts 06:00 | Deadline 08:00\n";
    std::cout << "[CORE] Friction multiplier: " << std::fixed << std::setprecision(2)
              << friction_multiplier << "x  → travel band ["
              << req.travel_low() << ", " << req.travel_high() << "] min\n\n";

    // ── Plugin dispatch ─────────────────────────────────────────────────────
    // g_registry.solve() selects ORToolsSolver (minimise) or DeterministicSolver
    // (worst-case band) depending on what was registered at bootstrap.
    // If the registered solver fails, it silently falls back to Deterministic.
    caelus::SolverResult res = g_registry.solve(req);

    // ── Print result ────────────────────────────────────────────────────────
    auto fmt = [](int m) -> std::string {
        char b[8];
        std::snprintf(b, sizeof(b), "%d:%02d", m / 60, m % 60);
        return b;
    };
    if (res.feasible) {
        std::cout << "[CORE] Solver: " << (res.on_time ? "ON TIME" : "AT RISK") << "\n";
        std::cout << "       Arrival time     : " << fmt(res.arrival) << "\n";
        std::cout << "       Completion time  : " << fmt(res.completion) << "\n";
        std::cout << "       OTP Status       : "
                  << (res.on_time ? "ON TIME" : "AT RISK / DELAYED") << "\n";
    } else {
        std::cout << "[CORE] Solver: No feasible solution — OTP AT RISK\n";
    }
    std::cout << "[CORE] ---------------------------------------------------\n";

    g_emitter.emit(caelus::ws_json::optimization_result(
        res.on_time, res.arrival, res.completion, friction_multiplier));

    return res;
}

