struct ConnectorIntelBridge {
    caelus::causal::CausalEngine* causal = nullptr;
};

static uint8_t InjectConnectorIntel(void* ctx, double friction_coeff,
                                    uint8_t crisis_level,
                                    const char* memo, size_t memo_len) noexcept {
    auto* bridge = static_cast<ConnectorIntelBridge*>(ctx);
    if (!bridge || !bridge->causal) return 0;

    char memo_buf[128] = {};
    if (memo && memo_len > 0) {
        const size_t n = std::min(memo_len, sizeof(memo_buf) - 1);
        std::memcpy(memo_buf, memo, n);
        memo_buf[n] = '\0';
    }

    bridge->causal->inject_intel(
        std::clamp(friction_coeff, 0.0, 1.0),
        static_cast<int>(std::min<uint8_t>(crisis_level, 3)),
        memo_buf);
    return 1;
}

